/**
 * Extract Flaticon Uicons glyph map from npm CSS and copy font into assets.
 * Run: node scripts/build-flaticon-icons.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const PKG = path.join(ROOT, 'node_modules', '@flaticon', 'flaticon-uicons');
const OUT_DIR = path.join(ROOT, 'assets', 'fonts', 'flaticon');
const GLYPHS_OUT = path.join(ROOT, 'constants', 'flaticon-glyphs.ts');

const STYLES = [
  {
    id: 'rr',
    css: path.join(PKG, 'css', 'regular', 'rounded.css'),
    fontFile: 'uicons-regular-rounded-KDJ23353.woff',
    fontFamily: 'FlaticonUicons-RegularRounded',
    exportName: 'flaticonRegularRoundedGlyphs',
  },
  {
    id: 'sr',
    css: path.join(PKG, 'css', 'solid', 'rounded.css'),
    fontFile: 'uicons-solid-rounded-UWVI4CEC.woff',
    fontFamily: 'FlaticonUicons-SolidRounded',
    exportName: 'flaticonSolidRoundedGlyphs',
  },
  {
    id: 'br',
    css: path.join(PKG, 'css', 'bold', 'rounded.css'),
    fontFile: 'uicons-bold-rounded-UTQQXXF3.woff',
    fontFamily: 'FlaticonUicons-BoldRounded',
    exportName: 'flaticonBoldRoundedGlyphs',
  },
];

function parseGlyphs(cssPath, prefix) {
  const css = fs.readFileSync(cssPath, 'utf8');
  const re = new RegExp(`\\.(${prefix}-([a-z0-9-]+)):before\\{content:"\\\\([0-9a-fA-F]+)"`, 'g');
  const glyphs = {};
  let match;
  while ((match = re.exec(css)) !== null) {
    const iconName = match[2];
    const codePoint = parseInt(match[3], 16);
    if (!Number.isNaN(codePoint)) {
      glyphs[iconName] = codePoint;
    }
  }
  return glyphs;
}

function copyFont(fontFile, styleId) {
  const src = path.join(PKG, 'css', fontFile);
  if (!fs.existsSync(src)) {
    throw new Error(`Font not found: ${src}`);
  }
  const dest = path.join(OUT_DIR, `${styleId}.woff`);
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.copyFileSync(src, dest);
  return dest;
}

function convertWoffToTtf(styleId) {
  const { spawnSync } = require('child_process');
  const woff = path.join(OUT_DIR, `${styleId}.woff`);
  const ttf = path.join(OUT_DIR, `${styleId}.ttf`);
  const script = `
from fontTools.ttLib import TTFont
font = TTFont(${JSON.stringify(woff)})
font.flavor = None
font.save(${JSON.stringify(ttf)})
`;
  const result = spawnSync('python', ['-c', script], { encoding: 'utf8' });
  if (result.status !== 0) {
    throw new Error(
      `woff→ttf failed for ${styleId}. Install fonttools: pip install fonttools\n${result.stderr || result.stdout}`,
    );
  }
}

function main() {
  if (!fs.existsSync(PKG)) {
    console.error('Install @flaticon/flaticon-uicons first: npm i @flaticon/flaticon-uicons');
    process.exit(1);
  }

  const exports = [];

  for (const style of STYLES) {
    const prefix = `fi-${style.id}`;
    const glyphs = parseGlyphs(style.css, prefix);
    const count = Object.keys(glyphs).length;
    if (count === 0) {
      throw new Error(`No glyphs parsed for ${style.id} — check CSS format`);
    }
    copyFont(style.fontFile, style.id);
    convertWoffToTtf(style.id);
    exports.push({ ...style, glyphs, count });
    console.log(`[flaticon] ${style.id}: ${count} icons → assets/fonts/flaticon/${style.id}.ttf`);
  }

  const ts = `// Auto-generated by scripts/build-flaticon-icons.js — do not edit manually.
// Uicons by Flaticon — https://www.flaticon.com/uicons

export type FlaticonStyle = 'rr' | 'sr' | 'br';

export const FLATICON_FONT_FILES = {
  rr: require('../assets/fonts/flaticon/rr.ttf'),
  sr: require('../assets/fonts/flaticon/sr.ttf'),
  br: require('../assets/fonts/flaticon/br.ttf'),
} as const;

export const FLATICON_FONT_FAMILIES = {
  rr: 'FlaticonUicons-RegularRounded',
  sr: 'FlaticonUicons-SolidRounded',
  br: 'FlaticonUicons-BoldRounded',
} as const;

${exports
  .map(
    (s) =>
      `export const ${s.exportName}: Record<string, number> = ${JSON.stringify(s.glyphs, null, 2)} as const;`,
  )
  .join('\n\n')}

export const FLATICON_GLYPH_MAPS = {
  rr: flaticonRegularRoundedGlyphs,
  sr: flaticonSolidRoundedGlyphs,
  br: flaticonBoldRoundedGlyphs,
} as const;
`;

  fs.mkdirSync(path.dirname(GLYPHS_OUT), { recursive: true });
  fs.writeFileSync(GLYPHS_OUT, ts, 'utf8');
  console.log(`[flaticon] Wrote ${GLYPHS_OUT}`);
}

main();
